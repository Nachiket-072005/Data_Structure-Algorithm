public class JavaBasic003_variables_in_Java {
    public static void main(String args[]) {
        int a = 10;
        int b = 5;

        System.out.println(a);
        System.out.println(b);

        String name = "Tony Stark";
        System.out.println(name);

        a = 50;
        System.out.println(a);
    }
}
