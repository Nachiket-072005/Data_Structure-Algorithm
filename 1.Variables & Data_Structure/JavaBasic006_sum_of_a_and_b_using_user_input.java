import java.util.*;

public class JavaBasic006_sum_of_a_and_b_using_user_input { 
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();
        //Sum of two numbers 
        int sum = a + b;
        System.out.println(sum);
    }
}
