public class JavaBasic027 {
    public static void main(String args[]) {
        int num = 4;

        switch (num) {
            case 1:
                System.out.println("To get Samosa");
                break;
            case 2:
                System.out.println("To get Burger");
                break;
            case 3:
                System.out.println("To get Milk Juice");
                break;
            default:
                System.out.println("Oops!! We are dreaming now.");
        }
    }
}
