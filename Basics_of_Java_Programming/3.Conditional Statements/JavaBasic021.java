import java.util.*;

public class JavaBasic021 {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        if(n % 2 == 0) {
            System.out.println("Even no");
        } else {
            System.out.println("Odd no");
        }
    }
}
